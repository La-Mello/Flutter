/**
    a demonstration of loops using dart
 */

void main(){

    //for..loop
    for(int i=0;i<10;i++)
        print("$i");
    
    //do ..while
    int i=0;
    do{
        print("$i");
        i++;
    }while(i<10);


    //while loop
    i=0;
    while(i<10){
        print("$i");
        i++;
    }

}